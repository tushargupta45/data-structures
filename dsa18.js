const calcSum = (i, j, k, mat) => {
  let sum = 0;
  for (let a = i - k; a <= i + k; a++) {
    for (let b = j - k; b <= j + k; b++) {
      if (mat[a] && mat[a][b]) {
        sum += mat[a][b];
      }
    }
  }
  return sum;
};

var matrixBlockSum = function (mat, k) {
  if (
    Math.min(k, mat.length, mat[0].length) < 1 ||
    Math.max(k, mat.length, mat[0].length) > 100
  ) {
    return [];
  }
  let result = [[]];
  for (let i = 0; i < mat.length; i++) {
    result.push([]);
    for (let j = 0; j < mat[0].length; j++) {
      if (mat[i][j] < 1 || mat[i][j] > 100) {
        return [];
      }
      result[i].push(calcSum(i, j, k, mat));
    }
  }
  result.pop();
  return result;
};

